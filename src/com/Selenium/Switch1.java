package com.Selenium;

public class Switch1 {

    public static void main(String[] args) {

        int a = 3;

        switch (a) {
            case 1:
                System.out.println("ta liczna to 1");
                break;
            case 2:
                System.out.println("ta liczba to 2");
                break;
            default:
                System.out.println("ta liczba to ani 1 ani 2 ");

        }


    }
}
